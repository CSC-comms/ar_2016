						Trustee, seek to minimise the potential adverse effects of these risks on the Scheme's financial performance. These policies may include the use of financial derivative 
						instruments.
					</p>
					<p>
						The Trustee is responsible for ensuring that there is an effective risk management control framework in place for the Scheme. Consistent with regulatory requirements, the 
