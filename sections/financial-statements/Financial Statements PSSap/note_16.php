							appropriate for the Plan and is in accordance with the Plan's published investment strategy. The Trustee applies strategies to manage risk relating to the investment 
							activities of the AIT. The investments of AIT are managed on behalf of the Trustee by specialist sector fund managers who are required to invest the assets in accordance 
