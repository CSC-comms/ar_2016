                <h2>DFRB</h2>
                <p>
                    DFRB is a closed military scheme with no contributing members. Established in 1948 by the DFRB Act, this scheme closed to new contributors on 30 September 1972 (contributing members at that time transferred to DFRDB on 1 October 1972). DFRB continues to provide for the benefit entitlements of members who ceased to be contributors before 1 October 1972 and for reversionary benefits to eligible spouses and children.
                </p>
                <h2>DFRDB</h2>
                <p>
                    DFRDB is also a closed military defined benefit scheme. Established by the DFRDB Act, the scheme closed to new ADF entrants on 1 October 1991 when MilitarySuper was established. DFRDB provides superannuation for ADF members who became contributors on or after 1 October 1972 and for contributors of DFRB on 30 September 1972 who compulsorily transferred to DFRDB on 1 October 1972.
                </p>
                <h2>DFSPB</h2>
                <p>
                    DFRDB members are also entitled to a productivity benefit under the Defence Force (Superannuation) (Productivity Benefit) Determination (DFSPB), issued under the <em>Defence Act 1903</em>. It is paid by the Department of Defence when a member’s DFRDB benefits are paid.
                </p>
