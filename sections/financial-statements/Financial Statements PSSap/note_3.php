			<h1><?php echo $title ?></h1>
			<p>
				The accounting policies set out below have been applied in preparing the financial statements for the year ended 30 June 2016 and the comparative information presented in 
				these financial statements for the year ended 30 June 2015.
			</p>
			<ol class="lower_alpha">
				<li>
					<h2>Assets</h2>
					<p>
						Assets are included in the Statement of Financial Position at net market value as at reporting date and movements in the net market value of assets are recognised in the 
						Operating Statement in the periods in which they occur. Net market value of investments includes a deduction for selling costs which would be expected to be incurred if 
						the investments were sold.
					</p>
					<p>
						Financial assets (being investments in a pooled superannuation trust, cash at bank and sundry debtors) are recognised on the date the Plan becomes a party to the 
						contractual provisions of the asset. Financial assets are recognised using trade date accounting. From this date, any gains and losses from changes in net market value 
						are recorded.
					</p>
					<p>
						Net market value means the amount which could be expected to be received from the disposal of an asset in an orderly market after deducting costs expected to be incurred 
						in realising the proceeds of such a disposal. As selling costs are generally immaterial, net market value approximates fair value unless otherwise stated.
					</p>
					<p>
						Net market values have been determined as follows:
					</p>
					<ol>
						<li>Units in a pooled superannuation trust are valued at the redemption price at close of business on the last business day of the reporting period as notified by the 
						manager of the trust, reflecting the net market value of the underlying investments.</li>
						<li>Sundry debtors are recognised at the amounts receivable. All amounts are unsecured and are subject to normal credit terms.</li>
					</ol>
				</li>
				<li>
					<h2>Cash and Cash Equivalents</h2>
					<p>
						Cash and cash equivalents includes cash at bank used to transact contributions, transfers to and from other funds, benefit payments and tax liabilities.
					</p>
				</li>
				<li>
					<h2>Foreign Currency Transactions</h2>
					<p>
						The Plan does not undertake transactions denominated in foreign currencies.
					</p>
				</li>
				<li>
					<h2>Payables</h2>
					<p>
						Payables (being benefits payable and sundry payables) are recognised at their nominal value which is equivalent to net market value.
					</p>
					<h3>Benefits payable</h3>
					<p>
						Benefits payable to a member are recognised where a valid withdrawal notice has been received from the employer sponsor, and approved by the Plan administrator ('Pillar'), 
						but payment has not been made by reporting date.
					</p>
					<h3>Sundry payables and amounts due to other schemes</h3>
					<p>
						Sundry payables represent liabilities for goods and services provided to the Plan during the financial period and which are unpaid at reporting date. All amounts are 
						unsecured. Creditors are subject to normal credit terms.
					</p>
				</li>
				<li>
					<h2>Operational risk reserve</h2>
					<p>
						The purpose of the operational risk reserve (ORR) is to provide adequate financial resources to address losses arising from an operational risk event. The ORR is 
						operated in accordance with an ORR policy. The level of the reserve is determined by the Trustee Directors and reviewed annually, based on an assessment of the risks 
						faced by the Fund. The transferred assets underlying the ORR are held in separate cash options of the ARIA Investments Trust and income earned on these assets is 
						recognised in the reserve.
					</p>				
				</li>
				<li>
					<h2>Derivatives</h2>
					<p>
						The Plan does not enter into derivative financial instruments.
					</p>
				</li>
				<li>
					<h2>Revenue</h2>
					<p>
						Revenue is recognised to the extent that it is probable that the economic benefits will flow to the entity and the revenue can be reliably measured. Specific revenues are 
						recognised as follows:
					</p>
					<h3>Investment revenue</h3>
					<p>
						Interest revenue is recognised on an accrual basis.
					</p>
					<p>
						Changes in the net market value of investments are recognised as income and are determined as the difference between the net market value (measured at fair value) at year 
						end or consideration received (if sold during the year) and the net market value (measured at fair value) as at the prior year end or cost (if the investment was acquired 
						during the period).
					</p>
					<h3>Contribution Revenue</h3>
					<p>
						Employer and member contributions, transfers from other funds and superannuation co-contributions and low income superannuation contributions from the Commonwealth Government 
						are recognised on a cash basis.
					</p>
				</li>
				<li>
					<h2>Expenses</h2>
					<p>
						Expenses are recognised on an accruals basis and, if not paid at reporting date, are reflected in the Operating Statement as an accrual or payable depending upon whether 
						or not the expense has been billed.
					</p>
				</li>
				<li>
					<h2>Insurance Premiums</h2>
					<p>
						Death and total & permanent disability insurance premiums are charged to member accounts on a monthly basis and then remitted to the life insurer in arrears.
					</p>
				</li>
				<li>
					<h2>Income Tax</h2>
					<p>
						Income tax on benefits accrued as a result of operations for the year comprises current and deferred tax. Income tax is recognised in the Operating Statement except to 
						the extent that it relates to items recognised directly in members' funds. As the Plan invests in the ARIA Investments Trust ('AIT'), which is a pooled superannuation 
						trust, tax on this investment revenue is paid by the AIT.
					</p>
					<h3>Current tax</h3>
					<p>
						Current tax is calculated by reference to the amount of income taxes payable or recoverable in respect of the taxable profit or loss for the period. It is calculated 
						using tax rates and tax laws that have been enacted or substantively enacted by reporting date. Current tax for the current period is recognised as a liability (or asset) 
						to the extent that it is unpaid (or refundable).
					</p>
					<h3>Deferred tax</h3>
					<p>
						Deferred tax is accounted for using the comprehensive balance sheet liability method in respect of temporary differences arising from the carrying amounts of assets 
						and liabilities in the financial statements and the corresponding tax base of those items.
					</p>
					<p>
						In principle, deferred tax liabilities are recognised for all taxable temporary differences. Deferred tax assets are recognised to the extent that it is probable that 
						sufficient taxable amounts will be available against which deductible temporary differences or unused tax losses and tax offsets can be utilised. However, deferred tax 
						assets and liabilities are not recognised if the temporary differences giving rise to them arise from the initial recognition of assets and liabilities (other than as a 
						result of a business combination) which affects neither taxable income nor accounting profit.
					</p>
					<p>
						Deferred tax assets and liabilities are measured at the tax rates that are expected to apply to the period(s) when the asset and liability giving rise to them are 
						realised or settled, based on tax rates (and tax laws) that have been enacted or substantively enacted by reporting date. The measurement of deferred tax liabilities 
						and assets reflects the tax consequences that would follow from the manner in which the Plan expects, at the reporting date, to recover or settle the carrying amount of 
						its assets and liabilities.
					</p>
					<p>
						Deferred tax assets and liabilities are offset when they relate to income taxes levied by the same taxation authority and the Plan intends to settle its current tax 
						assets and liabilities on a net basis.
					</p>
					<h3>Current and deferred tax for the period</h3>
					<p>
						Current and deferred tax for the period is recognised as an expense or benefit in the Operating Statement.
					</p>
				</li>
				<li>
					<h2>Goods and Services Tax ('GST')</h2>
					<p>
						Revenues, expenses and assets are recognised net of the amount of goods and services tax ('GST') recoverable from the Australian Taxation Office ('ATO') as a 
						reduced input tax credit. Where the amount of GST incurred is not recoverable from the ATO, it is recognised as part of the cost of acquisition of an asset or as 
						part of an expense item.
					</p>
					<p>
						Receivables and payables are recognised inclusive of GST.
					</p>
					<p>
						The net amount of GST recoverable from, or payable to, the ATO is included as an asset or liability in the Statement of Financial Position.
					</p>
				</li>
			</ol>
