					<h2>Market risk</h2>
					<p>
						Market risk is the risk that the fair value or future cash flows of a financial instrument will fluctuate because of changes in market prices. Market risk comprises three 
						types of risk: foreign currency risk, interest rate risk and other market price risk. The policies and procedures put in place to mitigate the exposure to market risk are 
						detailed in the Trustee's investment policies and the Risk Management Framework.
					</p>
					<p>
						There has been no change to the Fund's exposure to market risk or the manner in which it manages and measures that risk since the 2015 reporting period.
					</p>
					<h3>Foreign currency risk</h3>
					<p>
						Foreign currency risk is the risk that the net market value of future cash flows of a financial instrument will fluctuate because of changes in foreign exchange rates.
					</p>
					<p>
						The Fund does not undertake any transactions in foreign currency and is therefore not directly exposed to foreign currency risk. However, the Fund is indirectly exposed 
						to foreign currency risk from the international assets held in the AIT, and it is managed in accordance with the Trustee’s approved investment strategy. The AIT enters into 
						forward foreign exchange contracts to hedge into Australian dollars some of the currency exposure arising from its investments denominated in developed markets foreign 
						currencies. These contracts neutralise some of the gains and losses from currency fluctuation. A small part of the investments of the AIT, relating to emerging markets, may 
						remain unhedged due to lack of suitable currency instruments for hedging.
					</p>
