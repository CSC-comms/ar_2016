            <h3>Insurance benefits</h3>
            <p>
                During 2015–16, eligible PSSap members received an automatic level of death, total and permanent disability (TPD) and income protection cover, and could apply to vary, increase, decrease or opt out of their cover. These insurance arrangements are detailed below.
            </p>
            <p>
                From 1 October 2016, new insurance arrangements (called life<em>PLUS</em> cover) will be introduced, making the insurance cover offered through PSSap available to eligible PSSap preserved and Ancillary members for the first time. life<em>PLUS</em> cover has two offerings: life<em>PLUS</em> auto cover and life<em>PLUS</em> choice cover.
            </p>
            <p>
                All insurance cover for PSSap members (including life<em>PLUS</em> cover) that is detailed in this report is provided by the Insurer, AIA Australia Limited ABN 79 004 837 861, AFSL 230043).
