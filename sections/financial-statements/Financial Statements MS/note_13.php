					<h2>Financial risk management objectives</h2>
					<p>
						The Fund is exposed to a variety of financial risks as a result of its pooled investment in the AIT. These risks include market risk (including currency risk, fair 
						value interest rate risk and price risk), credit risk, liquidity risk and cash flow interest rate risk. The Fund's risk management and investment policies, approved 
						by the Trustee, seek to minimise the potential adverse effects of these risks on the Fund’s financial performance. These policies may include the use of financial 
						derivative instruments.
					</p>
					<p>
						The Trustee is responsible for ensuring that there is an effective risk management control framework in place for the Fund. Consistent with regulatory requirements, the 
						Trustee has developed, implemented and maintains a Risk Management Framework to identify the policies, procedures, processes and controls that comprise its risk 
						management and control systems for the Fund and for the Fund's investments through the AIT. The overall investment strategy of the Fund is set out in the Trustee's 
						approved investment policies which address the investment strategy and objectives and risk mitigation strategies including risk mitigation relating to the use of derivatives.
					</p>
					<p>
						The Trustee's internal investment team monitors and manages the financial risks relating to the Fund's investments. Derivative Risk Statements set out the strict 
						parameters for the Trustee's investment managers authorised to use derivatives. In essence, derivatives cannot be used to raise the level of risk above the level it 
						would otherwise have been, and derivatives cannot be used to leverage the investments.
					</p>
					<p>	
						The Fund's investments are managed on behalf of the Trustee by specialist external investment managers who invest their respective fund allocation in accordance with the 
						terms of a written investment mandate or disclosure document. The Trustee has determined that the appointment of these managers is appropriate for the Fund and is in 
						accordance with its investment strategy.
					</p>
