					<h2>Financial risk management objectives</h2>
					<p>
						The Plan is exposed to a variety of financial risks as a result of its pooled investments in the AIT. These risks include market risk (including currency risk, fair value 
						interest rate risk and price risk), credit risk, liquidity risk and cash flow interest rate risk. The Plan's risk management and investment policies, approved by the Trustee,
						seek to minimise the potential adverse effects of these risks on the Plan's financial performance. These policies may include the use of financial derivative instruments.
					</p>
					<p>
						The Trustee ensures that there is an effective risk management control framework in place for the Plan. Consistent with regulatory requirements, the Trustee has developed, 
						implemented and maintains a Risk Management Framework to identify the policies, procedures, processes and controls that comprise its risk management and control systems 
						for the Plan and for the Plan's investments through the AIT. The overall investment strategy of the Plan is set out in the Trustee's approved investment policies which 
						address the investment strategy and objectives and risk mitigation strategies including risk mitigation relating to the use of derivatives.
					</p>
					<p>
						The Trustee's internal investment team monitors and manages the financial risks relating to the Plan's investments. Derivative Risk Statements set out the strict parameters 
						for the Trustee's investment managers authorised to use derivatives. In essence, derivatives cannot be used to raise the level of risk above the level it would otherwise 
						have been, and derivatives cannot be used to leverage the investments.
					</p>
