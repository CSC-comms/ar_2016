            <h2>Governance of <em>Australian Government Superannuation Schemes Act 2011 (GAGSS Act)</em></h2>
            <p>
                The <em>Defence Legislation Amendment (Superannuation and ADF Cover) Act 2015</em> amended the GAGSS Act to incorporate the Australian Defence Force Superannuation Scheme (ADF Super) and the Australian Defence Force Cover Scheme (ADF Cover). The amendments under schedule 1 commenced on 11 September 2015.
            </p>
            <h3>Changes to <em>Public Governance, Performance and Accountability Act 2013</em></h3>
            <p>
                Following the merger of ComSuper with CSC on 1 July 2015, the<em>Public Governance, Performance and Accountability Amendment (CSC) Rule 2016</em> (Amendment Rule) was made to amend the rule instrument made under the <em>PGPA Act</em>, the <em>Public Governance, Performance and Accountability Rule 2014 (PGPA Rule)</em>. The Amendment Rule makes CSC subject to certain arrangements that applied to ComSuper when it performed the administrative services function in relation to the superannuation schemes administered by CSC. The Amendment Rule:
            </p>
            <ul>
                <li>
                    provides that, in relation to amounts received by CSC on behalf of the Commonwealth and certain other amounts, CSC must comply with directions relating to bank accounts opened and maintained in Australia given by the Finance Minister in delegating his or her power in subsection 53(1) of the PGPA Act to non-corporate Commonwealth entities. The amounts covered by the requirement include administration fees and amounts that CSC collects from agencies, on behalf of the Commonwealth, in line with government policy. In practice, this will mean that Commonwealth money and certain other money held in CSC bank accounts will continue to be consolidated at the end of each day and banking arrangements for this money will need to comply with the directions. CSC will not be required to apply the directions in managing money that the organisation holds in its own right;
                </li>
                <li>
                    provides that the function of providing administrative services in relation to the superannuation schemes administered by CSC, formerly performed by ComSuper, will continue to be subject to, and need to comply with, the <em>Commonwealth Procurement Rules – July 2014</em> (CPRs), now that this function is performed by CSC. The CPRs will not apply to CSC in respect of any of its other functions, such as fund management and investment management; and
                </li>
                <li>
                    allows the Board of CSC, which is a corporate Commonwealth entity, or an official of CSC to be delegated and the Board of CSC to sub-delegate certain powers, functions or duties relating to recovery of debts owing to the Commonwealth under the PGPA Act and the PGPA Rule. This is necessary given CSC’s function to provide administrative services in relation to the superannuation schemes administered by the organisation. The relevant powers, functions or duties were performed by ComSuper when it previously undertook this administrative services function. Debts owed to CSC in its own right are not affected by the Amendment Rule and will be managed in the same manner as has been CSC’s practice to date.
                </li>
            </ul>
            <p>
                The Amendment Rule commenced on 13 April 2016.
            </p>
            <h3>Establishment of new superannuation scheme &mdash; ADF Super</h3>
            <p>
                The <em>Australian Defence Force Superannuation Act 2015</em> gave effect to a new modern superannuation arrangement for people joining the Australian Defence Force from 1 July 2016. The Act established a new superannuation scheme known as ADF Super. ADF Super is a fully-funded accumulation scheme.
            </p>
            <p>
                The <em>Australian Defence Force Superannuation Trust Deed 2015</em> established the Trust Deed for ADF Super and sets out the functions and powers of CSC in relation to the scheme and the fund.
            </p>
            <h3>Establishment of new statutory death and invalidity scheme &mdash; ADF Cover</h3>
            <p>
                The <em>Australian Defence Force Cover Act 2015</em> established a new statutory death and invalidity scheme, to be known as ADF Cover. ADF Cover provides all members of the Australian Defence Force who join after 1 July 2016 with death and invalidity cover.
