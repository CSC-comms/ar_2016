					<h2>Liquidity risk</h2>
					<p>
						Liquidity risk is the risk that the Scheme will encounter difficulty in either realising assets or otherwise raising sufficient funds to meet its financial liabilities 
						and/or member benefit payments or tax liabilities.
					</p>
					<p>
						The Trustee's approach to managing liquidity is to ensure that the Fund will always have sufficient liquidity to meet its liabilities as they fall due. On resignation 
						the member benefit accrued before 30 June 1999 can be paid as a lump sum but the balance must be preserved until the member’s preservation age, either in the Fund or 
						another complying superannuation fund. The employer benefit, including productivity component, must be preserved in the Fund. The unfunded component of benefit payments 
						is financed by the Commonwealth, from the CRF. As such there is minimal liquidity risk.
					</p>
					<p>
						The Fund's exposure to liquidity risk is therefore limited to those circumstances in which the Scheme Rules allow members to withdraw benefits.
					</p>
					<p>
						The Trustee undertakes forecasting and scenario testing of the cashflow requirements of the Fund to ensure timely access to sufficient cash and holds actively-traded, 
						highly-liquid investments to meet anticipated funding requirements. As a further risk mitigation strategy, it is the Trustee's policy that the target asset allocation 
						to illiquid assets is limited to around 25% of the investments of the AIT (with a plus or minus 10 percentage point rebalancing range around that target). Regular 
						scenario testing is performed to confirm the validity of the strategy.
					</p>
