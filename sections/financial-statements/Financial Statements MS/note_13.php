					<h2>Financial instruments management</h2>
						<p>
							A financial instrument is any contract that gives rise to a financial asset of one entity and a financial liability or equity instrument of another entity.
						</p>
						<p>
							The investments of the Scheme (other than cash held for managing contribution receipts, benefit payments and tax payments) comprise units in the ARIA Investments 
							Trust ('AIT'). AIT is a pooled superannuation trust which is also governed by the Commonwealth Superannuation Corporation as Trustee. This type of investment has 
							been determined by the Trustee to be appropriate for the Scheme and is in accordance with the Scheme's published investment strategy. The Trustee applies strategies 
							to manage the risk relating to the investment activities of AIT. The investments of AIT are managed on behalf of the Trustee by specialist sector fund managers who 
							are required to invest the assets in accordance with contractual investment mandates.
