	<h3>Accounting policy</h3>
		<p>Liabilities for short-term employee benefits and termination benefits expected within twelve months of the end of reporting period are measured at their nominal amounts.</p>
	<h4>Leave</h4>
		<p>
			The liability for employee benefits includes provision for annual leave and long service leave. The leave liabilities are calculated on the basis of employees’ remuneration at the estimated salary rates that will be applied at the time the leave is taken, including the employer superannuation contribution rates to the extent that the leave is likely to be taken during service rather than paid out on termination. The liability for long service leave has been determined by reference to the Australian Government short hand method. The estimate of the present value of the liability takes into account attrition rates and pay increases through promotion and inflation.
		</p>
	<h4>Separation and Redundancy</h4>
		<p>
			The entity recognises a provision for separation and redundancy benefit payments when it has developed a detailed formal plan for the terminations and has informed those employees affected that it will carry out the terminations.
		</p>
	<h4>Superannuation</h4>
		<p>
			Staff are members of the Commonwealth Superannuation Scheme (CSS), the Public Sector Superannuation Scheme (PSS), or the PSS accumulation plan (PSSap), or other superannuation funds held outside the Australian Government. The CSS and PSS are defined benefit schemes for Australian Government employees. The PSSap is a defined contribution scheme.
		</p>
		<p>
			The liability for defined benefits is recognised in the financial statements of the Australian Government and is settled by the Australian Government in due course. This liability is reported in the Department of Finance’s administered schedules and notes.
		</p>
		<p>
			CSC makes employer contributions to the employees' defined benefit superannuation scheme at rates determined by an actuary to be sufficient to meet the current cost to the Government. CSC accounts for the contributions as if they were contributions to defined contribution plans.
		</p>
		<p>
			Any liability for superannuation recognised as at 30 June represents outstanding contributions.
		</p>
