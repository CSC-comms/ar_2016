						The Trustee's internal investment team monitors and manages the financial risks relating to the Scheme's investments. Derivative Risk Statements set out the strict 
						parameters for the Trustee's investment managers authorised to use derivatives. In essence, derivatives cannot be used to raise the level of risk above the level it 
						would otherwise have been, and derivatives cannot be used to leverage the investments.
