	<h3>Accounting Policy</h3>
		<h4>Financial assets</h4>
			<p>
				CSC classifies its financial assets as loans and receivables. The classification depends on the nature and purpose of the financial assets and is determined at the time of initial recognition. Financial assets are recognised and derecognised upon trade date.
			</p>
		<h4>Effective interest method</h4>
			<p>
				Income is recognised on an effective interest rate basis except for financial assets that are recognised at fair value through profit or loss.
			</p>			
		<h4>Impairment of financial assets</h4>
			<p>
				Financial assets are assessed for impairment at the end of each reporting period.
			</p>
			<p>
				If there is objective evidence that an impairment loss has been incurred for loans and receivables held at amortised cost, the amount of the loss is measured as the difference between the asset’s carrying amount and the present value of estimated future cash flows discounted at the asset’s original effective interest rate. The carrying amount is reduced by way of an allowance account. The loss is recognised in the Statement of Comprehensive Income.
			</p>
		<h4>Financial liabilities</h4>
			<p>
				Financial liabilities are classified as other financial liabilities. Financial liabilities are recognised and derecognised upon ‘trade date’.
			</p>
		<h4>Other financial liabilities</h4>
			<p>
				Other financial liabilities, including borrowings, are initially measured at fair value, net of transaction costs. These liabilities are subsequently measured at amortised cost using the effective interest method, with interest expense recognised on an effective interest basis.
			</p>
			<p>
				Supplier and other payables are recognised at amortised cost. Liabilities are recognised to the extent that the goods or services have been received (and irrespective of having been invoiced).
			</p>
