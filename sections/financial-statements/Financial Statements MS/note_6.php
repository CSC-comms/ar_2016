				<h2>Benefits</h2>
					<p>
						The benefits payable from the Scheme comprise a lump sum of accumulated member contributions and a defined benefit financed by the employer and calculated on the 
						basis of the member’s final average salary and length of service. The defined benefit may be taken as a lump sum or as a pension or as a combination of lump sum and 
						pension. The defined benefit consists of a funded component (the accumulated value of contributions made to the Fund by the Department of Defence) and an unfunded 
						component (the balance of the defined benefit).
					</p>
					<p>
						In general, when a benefit becomes payable to a member, the accumulated member and employer contributions held in the Fund in respect of the member are transferred to 
						the Consolidated Revenue Fund (CRF) which pays out the total benefit (both funded and unfunded components).
