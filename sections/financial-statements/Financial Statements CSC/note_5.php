	<h3>Accounting Policy</h3>
		<p>
			Assets are recorded at cost on acquisition except as stated below. The cost of acquisition includes the fair value of assets transferred in exchange and liabilities undertaken. Financial assets are initially measured at their fair value plus transaction costs where appropriate.
		</p>
		<p>
			Assets acquired at no cost, or for nominal consideration, are initially recognised as assets and income at their fair value at the date of acquisition, unless acquired as a consequence of restructuring of administrative arrangements. In the latter case, assets are initially recognised as contributions by owners at the amounts at which they were recognised in the transferor’s accounts immediately prior to the restructuring.
		</p>
		<h4>Asset Recognition Threshold</h4>
			<p>
				Purchases of property, plant and equipment are recognised initially at cost in the statement of financial position, except for purchases costing less than $2,000, which are expensed in the year of acquisition (other than where they form part of a group of similar items which are significant in total).
			</p>
			<p>
				The initial cost of an asset includes an estimate of the cost of dismantling and removing the item and restoring the site on which it is located. This is particularly relevant to ‘make good’ provisions where there exists an obligation to the lessor. These costs are included in the value of the entity's leasehold improvements with a corresponding provision for the ‘make good’ recognised.
			</p>
		<h4>Revaluations</h4>
			<p>
				Following initial recognition at cost, property, plant and equipment are carried at fair value less subsequent accumulated depreciation and accumulated impairment losses. Valuations are conducted with sufficient frequency to ensure that the carrying amounts of assets do not differ materially from the assets’ fair values as at the reporting date. The regularity of independent valuations depends upon the volatility of movements in market values for the relevant assets.
			</p>
			<p>
				Revaluation adjustments are made on a class basis. Any revaluation increment is credited to equity under the heading of asset revaluation reserve except to the extent that it reverses a previous revaluation decrement of the same asset class that was previously recognised in the surplus/deficit. Revaluation decrements for a class of assets are recognised directly in the surplus/deficit except to the extent that they reverse a previous revaluation increment for that class.
			</p>
			<p>
				Any accumulated depreciation as at the revaluation date is eliminated against the gross carrying amount of the asset and the asset restated to the revalued amount.
			</p>
		<h4>Depreciation</h4>
			<p>
				Depreciable property, plant and equipment assets are writtenoff to their estimated residual values over their estimated useful lives to the entity using, in all cases, the straight-line method of depreciation.
			</p>
			<p>
				Depreciation rates (useful lives), residual values and methods are reviewed at each reporting date and necessary adjustments are recognised in the current, or current and future reporting periods, as appropriate.
			</p>
