<h3>The Basis of Preparation</h3>
	<p>
		The financial statements are general purpose financial statements and are required by section 42 of the <em>Public Governance, Performance and Accountability Act 2013</em>.
	</p>
	<p>
		The financial statements have been prepared in accordance with:
	</p>
	<ol class="lower_alpha">
		<li><em>Public Governance, Performance and Accountability (Financial Reporting) Rule 2015</em> (FRR) for reporting periods ending on or after 1 July 2015; and</li>
		<li>Australian Accounting Standards and Interpretations issued by the Australian Accounting Standards Board (AASB) that apply for the reporting period.</li>
	</ol>
	<p>
		The financial statements have been prepared on an accrual basis and in accordance with the historical cost convention, except for certain assets and liabilities at fair value. Except where stated, no allowance is made for the effect of changing prices on the results or the financial position. The financial statements are presented in Australian dollars and values are rounded to the nearest thousand dollars unless otherwise specified.
	</p>
