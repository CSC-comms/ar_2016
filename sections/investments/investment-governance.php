	<p>
		CSC’s investment governance is focused on managing risk and is driven by our primary objective to achieve stated investment objectives within strictly defined risk limits. The CSC Board has agreed and established a comprehensive investment governance framework, which includes a clear statement of Board and Executive responsibilities.
	</p>
	<h2>The CSC Board</h2>
	<p>
		Sound and prudent management of the assets of the schemes is the responsibility of the Board. It sets, reviews and oversees the investment strategy, mission statement and core investment beliefs; approves and monitors investment strategies for each investment option; agrees the budget; and determines appropriate delegations.
	</p>
	<p>
		To approve CSC’s investment strategy, regard is had to factors such as CSC’s size as measured by funds under management and scheme membership, perceived competitive advantages, member demographics and the broader investment environment.
	</p>
	<p>
		To approve an investment strategy for an individual investment option, the Board considers the objective in terms of return and risk measures and the investment horizon.
	</p>
	<p>
		Management of investment activities is delegated by the Board to relevant staff. Reports are made to the Board on approved investment policies. Reports are also presented and discussed at every Board meeting on investment performance, liquidity, risk, manager and portfolio activity, portfolio structure, capital allocation and the risk budget.
	</p>
	<h2>CSC’s investment team</h2>
	<p>
		CSC’s investment team advises the Board on investments, implements Board-approved strategies and manages all investments within Board-approved delegations. Led by the Chief Investment Officer (CIO), the team manages the investments in a manner consistent with the Board’s investment strategy, decisions on asset allocation, and detailed investment policies.
	</p>
	<p>
		The team performs two major functions:
	</p>
	<ul>
		<li>Formulating investment strategy, option design, risk budget deployment and monitoring the evolving risks and opportunities in the Fund as well as the broader financial markets.</li>
		<li>Identifying the most efficient implementation channels for investment strategies, with ‘efficiency’ defined as the highest prospective, net return per unit of risk.</li>
	</ul>
	<p>
		Both functions are well resourced with specialist senior investment managers who report directly to the CIO and are supported by investment analysts.
	</p>
	<h2>CSC’s Investment Operations team</h2>
	<p>
		Responsibilities of the Investment Operations team include:
	</p>
	<ul>
		<li>implementation of investment team decisions, in accordance with Board-approved delegations</li>
		<li>management of the custodial relationship and its associated activities</li>
		<li>attribution of investment performance, and</li>
		<li>investment manager compliance and operational due diligence.</li>
	</ul>
	<p>
		The team is led by the General Manager (GM), Investment Operations. The GM, Investment Operations and the CIO report independently to the CEO.
	</p>
	<h2>Investment managers</h2>
	<p>
		Under scheme legislation, CSC is required to invest through external investment managers. On the recommendation of the Executive, the Board approves the appointment of managers as ‘investment grade managers’ that may be funded at any time by CSC.
	</p>	
	<p>
		Investment managers are identified for their specific expertise and invest according to individual mandates set by CSC which are designed to address CSC’s specific portfolio requirements. These mandates provide CSC’s directions on investment types to be held, the maximum and minimum holdings for each investment type and target rates of return and risk limits.
	</p>
	<p>
		Investment managers are paid a fee generally based, in part, on the value of assets managed on behalf of CSC, but importantly, where possible, on the basis of their performance both in terms of returns and risk taking.
	</p>
	<p>
		Fees reflect investment costs applicable to each particular asset class category and the investment style employed by each manager.
	</p>
	<p>
		Some managers may be paid a performance fee for exceeding a pre-determined benchmark or hurdle rate of return, within specified risk limits. The performance fee is generally a share of any excess risk-adjusted performance above an agreed benchmark return.
	</p>


